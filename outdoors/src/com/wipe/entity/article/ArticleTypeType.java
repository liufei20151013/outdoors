package com.wipe.entity.article;

public class ArticleTypeType {

	private Integer littleId;//小的户外类型Id
	private Integer bigId;//大的户外类型Id
	private String name;//活动名称
	public Integer getLittleId() {
		return littleId;
	}
	public void setLittleId(Integer littleId) {
		this.littleId = littleId;
	}
	public Integer getBigId() {
		return bigId;
	}
	public void setBigId(Integer bigId) {
		this.bigId = bigId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	
}
